package br.pucpr.servico;

public class produto {

    private int id;
    private String nome;
    private String codigo;



    public int getId() {

        return id;

    }



    public void setId(int id) {

        this.id = id;

    }



    public String getNome() {

        return nome;

    }



    public void setNome(String nome) {

        this.nome = nome;

    }



    public String getcodigo() {

        return codigo;

    }



    public void setcodigo(String codigo) {

        this.codigo = codigo;

    }


}
